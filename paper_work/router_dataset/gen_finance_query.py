def check_no_trigger(text: str) -> None:
    low = text.lower()
    for t in TRIGGERS:
        if t.lower() in low:
            raise AssertionError(f"含 trigger 子串 [{t}]！→ {text}")


def expand_pool(pool, targets) -> list[tuple[str, str]]:
    """{t} 用给定 targets 展开，并对无语气词结尾的句子追加 ？变体。"""
    out: list[tuple[str, str]] = []
    mood = set("吧呢嘛啊吗了呀哦呐")

    def add(exp: str, cat: str) -> None:
        out.append((exp, cat))
        tail = exp.rstrip()[-1] if exp.strip() else ""
        if tail and tail not in mood and tail not in "？。！，；":
            out.append((exp + "？", cat))

    for cat, sents in pool.items():
        for s in sents:
            if "{t}" in s:
                for tgt in targets:
                    add(s.replace("{t}", tgt), cat)
            else:
                add(s, cat)
    return out


def dedupe(items, seen=None):
    """去重 + trigger 校验；seen 用于跨池去重（test/val 优先占位，train 让路）。"""
    if seen is None:
        seen = set()
    out = []
    for text, cat in items:
        t = text.strip()
        if not t or t in seen:
            continue
        check_no_trigger(t)
        seen.add(t)
        out.append((t, cat))
    return out


def cap_per_split(items, target_n, seed):
    """打散后截断到目标量，按类配额轮转取，避免某类被砍光。"""
    rng = random.Random(seed)
    by_cat = {}
    for text, cat in items:
        by_cat.setdefault(cat, []).append((text, cat))
    for cat in by_cat:
        rng.shuffle(by_cat[cat])
    if len(items) <= target_n:
        out = list(items); rng.shuffle(out); return out
    out, cats = [], list(by_cat.keys())
    idx = {c: 0 for c in cats}
    while len(out) < target_n:
        progressed = False
        for c in cats:
            if idx[c] < len(by_cat[c]) and len(out) < target_n:
                out.append(by_cat[c][idx[c]]); idx[c] += 1; progressed = True
        if not progressed:
            break
    rng.shuffle(out)
    return out


def write_jsonl(path, items):
    with open(path, "w", encoding="utf-8") as fh:
        for text, cat in items:
            code, name = SUBCAT[cat]
            rec = {"text": text, "label": "finance-query", "subcat": f"{code}-{name}"}
            fh.write(json.dumps(rec, ensure_ascii=False) + "\n")


def main():
    base = Path(__file__).resolve().parent
    # 先建 test/val（优先占位），再建 train（让路，避免任何跨池重复）
    seen: set = set()
    test_raw = dedupe(expand_pool(POOL_TEST, T_TEST), seen)
    val_raw = dedupe(expand_pool(POOL_VAL, T_VAL), seen)
    train_raw = dedupe(expand_pool(POOL_TRAIN, T_TRAIN), seen)
    assert len(train_raw) >= 650, f"train 样本不足: {len(train_raw)} < 650"
    print(f"手写池展开+去重后：train={len(train_raw)} val={len(val_raw)} test={len(test_raw)}")

    test = cap_per_split(test_raw, 75, seed=20260711)
    val = cap_per_split(val_raw, 75, seed=20260712)
    train = cap_per_split(train_raw, 650, seed=20260713)

    write_jsonl(base / "train" / "finance-query.jsonl", train)
