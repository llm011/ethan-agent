def check_no_trigger(text: str) -> None:
    low = text.lower()
    for t in TRIGGERS:
        if t.lower() in low:
            raise AssertionError(f"含 trigger 子串 [{t}]！→ {text}")


def expand_pool(pool, with_mood=True) -> list[tuple[str, str]]:
    """展开一个池；train 用语气后缀扩量，val/test 保持原句更贴近真实。"""
    out: list[tuple[str, str]] = []
    mood = set("吧呢嘛啊吗了呀哦呐")
    for cat, sents in pool.items():
        for s in sents:
            out.append((s, cat))
            if not with_mood:
                continue
            tail = s.rstrip()[-1] if s.strip() else ""
            if tail and tail not in mood and tail not in "？。！，；":
                for suf in ["呢", "啊", "吗", "？"]:
                    out.append((s + suf, cat))
    return out


def dedupe(items, seen=None):
    """去重 + trigger 校验；seen 用于跨池去重（test/val 优先占位，train 让路）。"""
    if seen is None:
        seen = set()
    out = []
    for text, cat in items:
        t = text.strip()
        if not t or t in seen:
            continue
        check_no_trigger(t)
        seen.add(t)
        out.append((t, cat))
    return out


def cap_per_split(items, target_n, seed):
    """打散后截断到目标量，尽量保持子语义均衡。"""
    rng = random.Random(seed)
    by_cat = {}
    for text, cat in items:
        by_cat.setdefault(cat, []).append((text, cat))
    for cat in by_cat:
        rng.shuffle(by_cat[cat])
    if len(items) <= target_n:
        out = list(items)
        rng.shuffle(out)
        return out
    out, cats = [], list(by_cat.keys())
    idx = {c: 0 for c in cats}
    while len(out) < target_n:
        progressed = False
        for c in cats:
            if idx[c] < len(by_cat[c]) and len(out) < target_n:
                out.append(by_cat[c][idx[c]])
                idx[c] += 1
                progressed = True
        if not progressed:
            break
    rng.shuffle(out)
    return out


def write_jsonl(path, items):
    with open(path, "w", encoding="utf-8") as fh:
        for text, cat in items:
            code, name = SUBCAT[cat]
            rec = {"text": text, "label": "getnote", "subcat": f"{code}-{name}"}
            fh.write(json.dumps(rec, ensure_ascii=False) + "\n")


def main():
    base = Path(__file__).resolve().parent
    # 先建 test/val（优先占位），再建 train（让路，避免任何跨池重复）
    seen: set = set()
    test_raw = dedupe(expand_pool(POOL_TEST, with_mood=False), seen)
    val_raw = dedupe(expand_pool(POOL_VAL, with_mood=False), seen)
    train_raw = dedupe(expand_pool(POOL_TRAIN, with_mood=True), seen)

    test = cap_per_split(test_raw, 75, seed=20260711)
    val = cap_per_split(val_raw, 75, seed=20260712)
    train = cap_per_split(train_raw, 500, seed=20260713)
    write_jsonl(base / "train" / "getnote.jsonl", train)
    write_jsonl(base / "val" / "getnote.jsonl", val)
    write_jsonl(base / "test" / "getnote.jsonl", test)
    print(f"train={len(train)} val={len(val)} test={len(test)}")
    for name, split in [("train", train), ("val", val), ("test", test)]:
        c = Counter(SUBCAT[cat][0] for _, cat in split)
        print(f"\n{name} 子语义分布（共 {len(split)}）：")
        for cat in SUBCAT:
            code = SUBCAT[cat][0]
            print(f"  {code}-{SUBCAT[cat][1]:<6} {c.get(code, 0):>3}")


if __name__ == "__main__":
    main()
